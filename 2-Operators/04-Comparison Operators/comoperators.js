
// Comparison Operators

let x = 1;

// Relational
console.log(x > 0);
// Comparison operator is a boolean,
// it's true or false.
console.log(x >= 1);
console.log(x < 1);
// So this item is false, because x is not less than 1.
console.log(x <= 1);

// Equality
console.log(x === 1);
// So we have triple equal signs.
console.log(x !== 1);